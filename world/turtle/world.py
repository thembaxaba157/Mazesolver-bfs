def mazerunner(edge):
    """MAZERUNNER
     =============
     
     - Gets the commands to shortest path of the specified edge
     - return a list consisting of commands"""
     
    global position_x,position_y,directions,current_direction_index, robs_obs
    mazerun.visited_nodes = []
    mazerun.init_position_x = position_x
    mazerun.init_position_y = position_y
    mazerun.edge = edge
    mazerun.init_direction = directions[current_direction_index]
    mazerun.obstacles = robs_obs

    path = mazerun.mazerunner()
    mazerun.visited_nodes = []
    mazerun.obstacles = []
    return path
    


